# üçé For Loop Example in Python

# A list of fruits
fruits = ['apple', 'peach', 'pear']

# Loop through each fruit in the list
for fruit in fruits:
    print(fruit)                 # Prints the current fruit
    print(fruit + " pie")        # Adds 'pie' after each fruit
    print(fruits)                # Prints the entire list in each iteration

# After the loop finishes
print(fruits)                    # Prints the full list one final time

# Basic Arithmetic Operations
print(7 - 3)    # Subtraction → Output: 4
print(4 * 2)    # Multiplication → Output: 8
print(8 / 2)    # Division (returns float) → Output: 4.0
print(10 % 3)   # Modulus (remainder) → Output: 1
print(6 // 3)   # Floor Division (integer division) → Output: 2
print(2 ** 3)   # Exponentiation (power) → Output: 8

# Operator Precedence in Python (PEMDASLR)
# P → Parentheses ()
# E → Exponents **
# M/D → Multiplication or Division (left to right)
# A/S → Addition or Subtraction (left to right)

# Example of operator precedence
print(3 * 3 + 3 / 3 - 3)   # 9 + 1 - 3 → 7
